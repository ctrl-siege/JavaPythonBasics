class DataTypes {
    public static void main(String[] args) {
        int age = 20;
        double price = 9.99;
        char grade = 'A';
        boolean passed = true;
        String name = "John";

        System.out.println("Age: " + age);
        System.out.println("Price: " + price);
        System.out.println("Grade: " + grade);
        System.out.println("Passed: " + passed);
        System.out.println("Name: " + name);
    }
}